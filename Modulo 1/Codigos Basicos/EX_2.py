# LOOP WHILE

lista_frut= ['maçã', 'banana', 'morango', 'uva', 'laranja']

contador = 0
while contador < len(lista_frut):
    print(lista_frut[contador])
    contador=contador +1 






# LOOP FOR
for t in lista_frut:
    print(t)


